// Assemble the committed review-loop.workflow.js from its source parts.
//
// The ultracode Workflow sandbox cannot `import` sibling modules at runtime, so
// the deterministic adjudication (adjudication.mjs) and prompt helpers
// (prompts.mjs) are INLINED into a single self-contained script. This keeps
// adjudication.mjs the single, node-tested source of truth: the workflow file
// is a generated artifact, and a CI freshness check (--check) forbids drift —
// same discipline as the golden fixtures.
//
//   node build-workflow.mjs --write   # (re)generate review-loop.workflow.js
//   node build-workflow.mjs --check   # fail if the committed file is stale
//
// The template (review-loop.template.js) holds `export const meta` + the
// orchestration and marks the injection point with `// @@INLINE@@`.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const TEMPLATE = join(HERE, "review-loop.template.js");
const OUTPUT = join(HERE, "review-loop.workflow.js");
const INLINE_SOURCES = ["adjudication.mjs", "prompts.mjs"];
const MARKER_TOKEN = "@@INLINE@@";
// Match the ENTIRE marker line so no trailing text on it can leak into code.
const MARKER_LINE = /^[^\n]*@@INLINE@@[^\n]*$/m;

// Strip ES module syntax so the source can live as plain top-level declarations
// inside the workflow script: drop `export ` prefixes and any import lines.
function stripModuleSyntax(src) {
  return src
    .split("\n")
    .filter((line) => !/^\s*import\s.+from\s.+;?\s*$/.test(line))
    .map((line) => line.replace(/^export\s+/, ""))
    .join("\n")
    .trim();
}

function build() {
  const template = readFileSync(TEMPLATE, "utf8");
  if (!MARKER_LINE.test(template)) {
    throw new Error(`template is missing the ${MARKER_TOKEN} injection marker`);
  }

  const inlined = INLINE_SOURCES.map((name) => {
    const src = stripModuleSyntax(readFileSync(join(HERE, name), "utf8"));
    return `// ===== inlined from ${name} (generated; edit ${name}, then rebuild) =====\n${src}`;
  }).join("\n\n");

  const banner =
    "// AUTO-GENERATED by build-workflow.mjs — DO NOT EDIT BY HAND.\n" +
    "// Source: review-loop.template.js + adjudication.mjs + prompts.mjs.\n" +
    "// Regenerate: node build-workflow.mjs --write\n";

  const generated = banner + "\n" + template.replace(MARKER_LINE, inlined) + "\n";
  if (generated.includes(MARKER_TOKEN)) {
    throw new Error("injection marker survived into generated output");
  }
  return generated;
}

function main() {
  const mode = process.argv[2];
  const generated = build();

  if (mode === "--write") {
    writeFileSync(OUTPUT, generated);
    console.log(`wrote ${OUTPUT}`);
    return 0;
  }
  if (mode === "--check") {
    let committed = "";
    try {
      committed = readFileSync(OUTPUT, "utf8");
    } catch {
      console.error(`missing: ${OUTPUT} (run --write)`);
      return 1;
    }
    if (committed !== generated) {
      console.error(
        "STALE: review-loop.workflow.js differs from its sources. " +
          "Run `node build-workflow.mjs --write` and commit.",
      );
      return 1;
    }
    console.log("review-loop.workflow.js is in sync with its sources.");
    return 0;
  }
  console.error("usage: node build-workflow.mjs --write|--check");
  return 2;
}

process.exit(main());
