// Assemble the committed *.workflow.js scripts from their source parts.
//
// The ultracode Workflow sandbox cannot `import` sibling modules at runtime, so
// the deterministic adjudication (adjudication.mjs) and prompt helpers
// (prompts.mjs) are INLINED into a single self-contained script. This keeps
// adjudication.mjs the single, node-tested source of truth: the workflow file
// is a generated artifact, and a CI freshness check (--check) forbids drift —
// same discipline as the golden fixtures.
//
//   node build-workflow.mjs --write   # (re)generate every workflow
//   node build-workflow.mjs --check   # fail if any committed file is stale
//
// Each template (e.g. review-loop.template.js) holds `export const meta` + the
// orchestration and marks the injection point with `// @@INLINE@@`. Add a new
// workflow by appending an entry to TARGETS.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));

// Every generated workflow: its template, output file, and the ordered libraries
// inlined into it. loop-engine.mjs depends on adjudication/prompts/workflow-helpers,
// so those precede it; pipeline-auto additionally inlines the TDD, escalation, and
// integrator helpers it orchestrates.
const TARGETS = [
  {
    name: "review-loop",
    template: "review-loop.template.js",
    output: "review-loop.workflow.js",
    inline: ["adjudication.mjs", "prompts.mjs", "workflow-helpers.mjs", "loop-engine.mjs"],
  },
  {
    name: "pipeline-auto",
    template: "pipeline-auto.template.js",
    output: "pipeline-auto.workflow.js",
    inline: [
      "adjudication.mjs",
      "prompts.mjs",
      "workflow-helpers.mjs",
      "loop-engine.mjs",
      "tdd-prompts.mjs",
      "escalation-broker.mjs",
      "integrator-prompts.mjs",
      "prod-prompts.mjs",
    ],
  },
];

const MARKER_TOKEN = "@@INLINE@@";
// Match the ENTIRE marker line so no trailing text on it can leak into code.
const MARKER_LINE = /^[^\n]*@@INLINE@@[^\n]*$/m;

// Strip ES module syntax so the source can live as plain top-level declarations
// inside the workflow script: drop import statements (single- OR multi-line —
// inlined siblings resolve in-scope) and `export ` prefixes.
function stripModuleSyntax(src) {
  return src
    // `import ... from "...";` spanning one or more lines
    .replace(/^[ \t]*import\b[\s\S]*?\bfrom\s*['"][^'"]+['"]\s*;?[ \t]*$/gm, "")
    // bare side-effect import: `import "...";`
    .replace(/^[ \t]*import\s+['"][^'"]+['"]\s*;?[ \t]*$/gm, "")
    // drop the `export ` keyword, preserving indentation
    .replace(/^([ \t]*)export\s+/gm, "$1")
    .trim();
}

function build(target) {
  const template = readFileSync(join(HERE, target.template), "utf8");
  if (!MARKER_LINE.test(template)) {
    throw new Error(`${target.template} is missing the ${MARKER_TOKEN} injection marker`);
  }

  const inlined = target.inline.map((name) => {
    const src = stripModuleSyntax(readFileSync(join(HERE, name), "utf8"));
    return `// ===== inlined from ${name} (generated; edit ${name}, then rebuild) =====\n${src}`;
  }).join("\n\n");

  const banner =
    "// AUTO-GENERATED by build-workflow.mjs — DO NOT EDIT BY HAND.\n" +
    `// Source: ${target.template} + ${target.inline.join(" + ")}.\n` +
    "// Regenerate: node build-workflow.mjs --write\n";

  const generated = banner + "\n" + template.replace(MARKER_LINE, inlined) + "\n";
  if (generated.includes(MARKER_TOKEN)) {
    throw new Error(`injection marker survived into generated output for ${target.name}`);
  }
  return generated;
}

function main() {
  const mode = process.argv[2];

  if (mode === "--write") {
    for (const target of TARGETS) {
      writeFileSync(join(HERE, target.output), build(target));
      console.log(`wrote ${target.output}`);
    }
    return 0;
  }
  if (mode === "--check") {
    let stale = 0;
    for (const target of TARGETS) {
      const generated = build(target);
      let committed = "";
      try {
        committed = readFileSync(join(HERE, target.output), "utf8");
      } catch {
        console.error(`missing: ${target.output} (run --write)`);
        stale++;
        continue;
      }
      if (committed !== generated) {
        console.error(
          `STALE: ${target.output} differs from its sources. ` +
            "Run `node build-workflow.mjs --write` and commit.",
        );
        stale++;
      }
    }
    if (stale) return 1;
    console.log(`all ${TARGETS.length} workflow(s) in sync with their sources.`);
    return 0;
  }
  console.error("usage: node build-workflow.mjs --write|--check");
  return 2;
}

process.exit(main());
